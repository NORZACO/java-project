
public class Main {
    public static void main(String[] args) {
        // A demonstration of how to declare variables of other types:
        int myNum = 5;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";

        System.out.println(
            "myNum: " + myNum + "\n" +
            "myFloatNum: " + myFloatNum + "\n" +
            "myLetter: " + myLetter + "\n" +
            "myBool: " + myBool + "\n" +
            "myText: " + myText + "\n"
        );
    }
}
